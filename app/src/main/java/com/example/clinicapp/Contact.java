package com.example.clinicapp;

public class Contact {
    String name;
    String image;
    String phone;
    String especialidad;
    String logo;

    public Contact() {
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getPhone() {
        return phone;
    }

    public String getEspecialidad() {
        return especialidad;
    }

    public String getLogo() {
        return logo;
    }
}